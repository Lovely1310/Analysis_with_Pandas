"""##🔹 Broadcasting

👉 Broadcasting means:

NumPy automatically expands smaller arrays to match bigger ones

👉 Rules (remember this order):

Compare shapes from right to left
Each dimension must be:
Same size ✅ OR
One of them is 1 ✅
Otherwise ❌ error
Missing dimensions are treated as 1
Dimension with 1 gets stretched

👉 One-line memory trick:

“Same or 1 → OK, else → Error”
"""

