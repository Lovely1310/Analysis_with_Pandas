import numpy as np

# Case 1: Scalar broadcasting
a = np.array([1, 2, 3])
print("Scalar broadcasting:", a + 10)
# 10 becomes [10,10,10]

# Case 2: Different dimensions
A = np.array([[1], [2], [3]])   # shape (3,1)
B = np.array([10, 20, 30, 40])  # shape (4,)

print("\nShapes before broadcasting:")
print("A shape:", A.shape)
print("B shape:", B.shape)

# Broadcasting happens here
result = A + B

print("\nResult after broadcasting:")
print(result)

# Explanation:
# A expands column-wise → (3,4)
# B expands row-wise → (3,4)

# Case 3: Higher dimension
C = np.ones((2,3,1))   # shape (2,3,1)
D = np.array([1,2,3])  # shape (3,)

print("\nBroadcasting in higher dimension:")
print("Result shape:", (C + D).shape)
# Final shape will be (2,3,3)

# Case 4: Broadcasting FAIL
E = np.ones((3,4))
F = np.ones((2,4))

# This will give error
# print(E + F)

